//Print sum of nos. upto 20 using for loop.

public class Five {
    public static void main(String[] args) {
        int i,sum=0;

        for(i=1;i<=20;i++)
        {
            sum = sum + i;
            
        }
        System.out.print("Sum of nos upto 20 is "+sum);
    }
    
}
    

